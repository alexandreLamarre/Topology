package Algo;

public class Sort {
    /**
     * Standard helper method to print all the contents of an array
     * @param arr array to be printed to standard output
     */
    static void printArray(int arr[])
    {
        int n = arr.length;
        for (int i=0; i<n; ++i)
            System.out.print(arr[i]+" ");
        System.out.println();
    }
}
